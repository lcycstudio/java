public class Variables {
    public static void main(String[] args) {
        int myFirstNumber = 5;
        System.out.println(myFirstNumber);

        myFirstNumber = 10;
        System.out.println(myFirstNumber);

        myFirstNumber = 100;
        System.out.println(myFirstNumber);

        myFirstNumber = 1000;
        System.out.println(myFirstNumber);
    }
}
